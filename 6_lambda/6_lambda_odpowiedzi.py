# ── Zadanie 1 ────────────────────────────────
szescian = lambda x: x ** 3
print(szescian(4))

# ── Zadanie 2 ────────────────────────────────
# wyrazenie warunkowe (ternary) zmiesci sie w jednej linii lambdy
wieksza = lambda a, b: a if a > b else b
print(wieksza(7, 4))
